#include <stdio.h>   // Header file (standard input-output)

int main() {
    // Variable create
    int age = 20;          // integer variable (it stores numbers)
    float pi = 3.14;       // decimal
    char grade = 'A';      // for single character 

    // Print
    printf("Hello Students!\n");     // simple text print
    printf("Age = %d\n", age);       // %d integer 
    printf("Pi = %f\n", pi);         // %f float 
    printf("Grade = %c\n", grade);   // %c character 

    return 0;
}
